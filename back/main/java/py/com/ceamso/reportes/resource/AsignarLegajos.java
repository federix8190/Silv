package py.com.ceamso.reportes.resource;

/**
 *
 * @author konecta
 */
public class AsignarLegajos {

    private Integer anho;
    private Integer mes;
    private Long cedulaIdentidad;
    private Long idCpt;
    private Long idCptF;
    private Long idCptE;
    private Long idCeo;
    
    public AsignarLegajos() {
    }

    public Integer getAnho() {
        return anho;
    }

    public void setAnho(Integer anho) {
        this.anho = anho;
    }

    public Integer getMes() {
        return mes;
    }

    public void setMes(Integer mes) {
        this.mes = mes;
    }

    public Long getCedulaIdentidad() {
        return cedulaIdentidad;
    }

    public void setCedulaIdentidad(Long cedulaIdentidad) {
        this.cedulaIdentidad = cedulaIdentidad;
    }

    public Long getIdCpt() {
        return idCpt;
    }

    public void setIdCpt(Long idCpt) {
        this.idCpt = idCpt;
    }

    public Long getIdCptF() {
        return idCptF;
    }

    public void setIdCptF(Long idCptF) {
        this.idCptF = idCptF;
    }

    public Long getIdCptE() {
        return idCptE;
    }

    public void setIdCptE(Long idCptE) {
        this.idCptE = idCptE;
    }

    public Long getIdCeo() {
        return idCeo;
    }

    public void setIdCeo(Long idCeo) {
        this.idCeo = idCeo;
    }
        
}
